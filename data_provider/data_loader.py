class Dataset_Pred(Dataset):
    def __init__(self, root_path, data_path, flag, size,
                 features, target, timeenc, freq):
        scale = False
        # size [seq_len, label_len, pred_len]
        # info
        self.seq_len = size[0]
        self.label_len = size[1]
        self.pred_len = size[2]
        # init
        assert flag in ['pred']

        self.features = features  # M
        self.target = target
        self.scale = scale  # 是否标准化处理
        self.timeenc = timeenc
        self.freq = freq  # 月

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path, self.data_path))

        border1 = 0
        border2 = len(df_raw)-self.pred_len

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            self.scaler.fit(df_data.values)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        tmp_stamp = df_raw[['date']][border1:border2]
        tmp_stamp['date'] = pd.to_datetime(tmp_stamp.date)
        # 这里的起始日期是 tmp_stamp.date.values[-1]，即 tmp_stamp 中最后一个日期值，
        # periods=self.pred_len + 1 表示生成的日期序列长度为预测长度加 1，因为包含了起始日期，
        # freq=self.freq 则指定了日期序列的频率，即日期之间的间隔。
        pred_dates = pd.date_range(tmp_stamp.date.values[-1], periods=self.pred_len + 1, freq=self.freq)

        df_stamp = pd.DataFrame(columns=['date'])  # 创建一个新的 DataFrame df_stamp，用于存储日期序列
        df_stamp.date = list(tmp_stamp.date.values) + list(pred_dates[1:])  # 获得输入的时间加预测的时间
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            df_stamp['minute'] = df_stamp.date.apply(lambda row: row.minute, 1)
            df_stamp['minute'] = df_stamp.minute.map(lambda x: x // 15)
            data_stamp = df_stamp.drop(['date'], 1).values
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0)

        self.data_x = data[border1:border2]
        self.data_y = data
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        s_begin = index

        seq_x = self.data_x[s_begin:]
        seq_y = self.data_y
        seq_x_mark = self.data_stamp

        return seq_x, seq_y, seq_x_mark

    def __len__(self):
        return 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)
